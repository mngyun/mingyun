
public class SuffixConst {
    public static void main(String[] args) {
        double e1 = 7.125; // double variable = double const
        float e2 = 7.125F; // float variable = (float) double const

        long n1 = 1000000000000L; // longe variable = (long) int const
        long n2 = 120; // long varible = int const

        System.out.println(e1); // normal
        System.out.println(e2); // normal
        System.out.println(n1); // normal
        System.out.println(n2); // normal

    }
}