
public class SwitchBasic {
    public static void main(String[] args) {
        int selection = 4;

        switch (selection) {
            default:
                System.out.println("Java default");
                // break;
            case 1:
                System.out.println("Java 1");
                // break;
            case 2:
                System.out.println("Java 2");
                // break;
            case 3:
                System.out.println("Java 3");
                // break;

        }
    }

}